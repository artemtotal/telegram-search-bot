def _send_answer(message, text: str) -> None:
    text = text.replace("**", "").replace("*", "\u2022")
    MAX = 3900
    if len(text) <= MAX:
        message.reply_text(text, parse_mode=None)
        return
    parts = text.split("\n\n")
    chunk = ""
    for part in parts:
        if len(chunk) + len(part) + 2 > MAX:
            if chunk:
                message.reply_text(chunk.strip(), parse_mode=None)
            chunk = part
        else:
            chunk = chunk + "\n\n" + part if chunk else part
    if chunk:
        message.reply_text(chunk.strip(), parse_mode=None)


def _create_wait_indicator(update: Update):
    """Acknowledge a long-running request with a removable status message."""
    try:
        return update.message.reply_text(
            "вҸі Р—Р°РҝРёСӮ РҫСӮСҖРёРјР°РҪРҫ. РЁСғРәР°СҺ РІС–РҙРҝРҫРІС–РҙСҢ, РұСғРҙСҢ Р»Р°СҒРәР°, Р·Р°СҮРөРәР°Р№СӮРөвҖҰ",
            parse_mode=None,
        )
    except Exception as exc:
        logger.warning("Failed to create wait indicator: %s", exc)
        return None


